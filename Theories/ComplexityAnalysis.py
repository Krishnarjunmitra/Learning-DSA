# Big O, Big Theta, Big Omega (conceptual)
def big_o_example():
    n = 100
    for i in range(n):
        print(i)  # O(n)

def big_theta_example():
    n = 100
    for i in range(n):
        print(i)  # Θ(n)

def big_omega_example():
    n = 100
    for i in range(n):
        print(i)  # Ω(n)

